# ============================================================
# DATA TYPES vs DATA STRUCTURES
# ------------------------------------------------------------
# Data Type        → Defines the kind of value stored in a variable.
# Data Structure   → Defines how multiple data values are organized
#                    and accessed efficiently.
#
# Example idea:
# int → single value type
# list → structure that stores multiple values
# ============================================================


# ============================================================
# CORE PYTHON DATA STRUCTURES
# ============================================================

# --- SEQUENCE STRUCTURES (ordered collections) ---
# list        → Dynamic array; ordered and mutable collection
# tuple       → Fixed ordered collection; immutable
# str         → Immutable sequence of characters (text data)
# range       → Memory-efficient numeric sequence generator
# bytes       → Immutable sequence for binary data
# bytearray   → Mutable binary data container


# --- SET STRUCTURES (unique element collections) ---
# set         → Unordered collection storing unique elements
# frozenset   → Immutable version of set for fixed unique data


# --- MAPPING STRUCTURE (key–value storage) ---
# dict        → Hash table mapping keys to values for fast lookup


# ============================================================
# SPECIALIZED DATA STRUCTURES (collections module)
# ============================================================

# deque       → Double-ended queue optimized for fast insert/remove
# Counter     → Dictionary subclass for counting hashable objects
# defaultdict → Dictionary with automatic default values
# OrderedDict → Dictionary with explicit order control
# ChainMap    → Combines multiple dictionaries into one view
# namedtuple  → Tuple with named fields for structured records


# ============================================================
# QUEUE / PRIORITY STRUCTURES
# ============================================================

# Queue          → Thread-safe FIFO queue
# LifoQueue      → Stack-style queue (LIFO)
# PriorityQueue  → Queue that removes elements by priority


# ============================================================
# SPECIAL STRUCTURE
# ============================================================

# heap (heapq) → Priority heap used for efficient min/max retrieval
# ============================================================