# Take a variable x = 81. Use an assignment operator to change x to its square root.
x = int(input("enter number to find Square root: "))
x **= 0.5
print(f"Square root is {x}")
# You can find the square root using the exponentiation operator (n ** 0.5) 
# like (n)**1/2 similarly - (n)1/3 for Qube root